# 1) შევქმნათ 5 ელემენტიანი სი (list) და გადავიაროთ მას for ციკლით
my_list = [10, 20, 30, 40, 50]

for item in my_list:
    print(item)

# 2) შევქმნათ ცვლადი წინადადებით და გადავიაროთ მას for ციკლით თითოეული სიმბოლოს გამოსატანისთვის
sentence = "Hello, world!"

for char in sentence:
    print(char)




# 3) პირველ დავალებაში შექმნილი სი გადავიაროთ while ციკლით
index = 0                
length = len(my_list)          

while index < length:
    print(my_list[index])
    index += 1              







